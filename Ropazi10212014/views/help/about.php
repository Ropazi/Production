                    Ro-PAH-zee. It's a twist on "ropa" - the Spanish word for clothes. 
                    Ropazi was founded by a husband and wife who had trouble finding and buying stylish kid products at the right price for their 2-year-old, Haziq.
                    Sophia curated a Board of clothes, toys, and accessories for Haziq on Pinterest. There was only one problem - with all of the dead links on Pinterest, 
                    she could't purchase anything that she pinned!
                    Nauman, a self-proclaimed "fashion Nazi", was frustrated with all of the time he wasted looking for stylish clothes for Haziq. There was no single 
                    place to find stylish kid clothes, and no way to track the price of items that were out of their price range.
                    And thus, Ropazi was born. It's the only children-focused social commerce platform that helps users discover, share, 
                    and purchase products for kids - all in the same place.
                    Discover what other fashionistas are buying for kids of the same age. Share products that you find from all sorts of 
                    brands - from Ralph Lauren to Gymboree, and anything in between. And purchase those products when they go on sale with 
                    Ropazi's price-tracking notifications.
                    
