                                      <h3>What are clips?</h3>
                                      <div>
                                        <p>Clips are kids' products from the web that are shared on Ropazi. Every clip is purchasable and posted by other Ropazi users. Your clips are organized in the Catalogs that you create for you and others to see what kids' products you're into. One last thing - Ropazi automatically tracks the prices of your clips and notifies you when they go on sale to save you time and money. </p>
                                      </div>
                                      <h3>What are catalogs?</h3>
                                      <div>
                                        <p>Catalogs are collections of clips built by Ropazi users. For example, one user might have separate Catalogs for each of their kids, another user might organize their clips by category (Shoes, Toys, etc.), and another user might have separate Catalogs by season (Halloween, Back-To-School, etc.). You can also mix up a combination of these categories, or dream up something else altogether- it's entirely up to you! </p>
                                      </div>
                                      <h3>How do I search for products?</h3>
                                      <div>
                                        <p>Say goodbye to ugly sidebars full of filters that take five minutes to navigate. Our intuitive fill-in-the-blank style of searching allows you to filter out all the products that you aren't interested in and search by multiple filters at once. Just click on the drop-down menus for product type, price range, child age, and child gender to find exactly what you're looking for. (Alternatively, you can enter a search term in the traditional search bar near the top left of your page.)</p>
                                      </div>
                                      <h3>How do I clip something that's already on Ropazi?</h3>
                                      <div>
                                        <p>To clip something that's already on Ropazi, simply hover over an image while browsing Ropazi and click on the green scissors-shaped icon. Once you choose a Catalog to clip the product to, it will show up on your profile!</p>
                                      </div>
                                      <h3>How can I clip something that I found on the web?</h3>
                                      <div>
                                        <p>To clip something from the web, you must be on a product page from one of our many accepted brands. Then, click on the Ropazi bookmarklet in your browser, confirm that product that you want to clip, and choose one of your Catalogs for the clip. If you don't have the bookmarklet, you can easily download it here (LINK). Alternatively, you can click on the "Clip" button in Ropazi (the scissors icon near the top right of any Ropazi page) and follow the same process.</p>
                                      </div>
                                      <h3>How do I track the price of a product?</h3>
                                      <div>
                                        <p>To track the price of a product, all you have to do is clip it! We'll let you know when any of your clipped products drop in price so you can buy them when they go on sale.</p>
                                      </div>
                                      <h3>How do I purchase a product?</h3>
                                      <div>
                                        <p>To purchase a product, hover over the price of any clip in Ropazi, click the "Buy" button, and buy it on the product page on the main brand's website. It's as simple as that.</p>
                                      </div>
                                      <h3>How do I follow another user?</h3>
                                      <div>
                                        <p>You can follow and unfollow users by going to their profile and clicking on the Heart icon to the left side of the user's name. A light colored heart means that you do not currently follow this user whereas a dark colored heart means that you are. Once you start following a user, you will start receiving their Clippings in your feed.</p>
                                      </div>
                                      <h3>How do I follow a brand that I like?</h3>
                                      <div>
                                        <p>You can follow and unfollow your brand  by going to their profile and clicking on the Heart icon to the left side of the user's name. A light colored heart means that you do not currently follow this user whereas a dark colored heart means that you are.</p>
                                      </div>
                                      <h3>Why can't I clip a product?</h3>
                                      <div>
                                        <p>If you're having trouble clipping a product, it means that we are still in the process of integrating the Ropazi platform with that particular brand. Check back in a few days as we're continuously adding more sites to our growing list of accepted brands. We are working hard to make sure your favorite brands play well with Ropazi!</p>
